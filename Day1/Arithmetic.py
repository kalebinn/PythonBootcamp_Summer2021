# addition
x = 2 + 3
print(x)

# subtraction
y = 2 - 3
print(y)

# multiplication 
z = 2 * 3
print(z)

# division
d = 2 / 3
print(d)

# exponent 
e = 2 ** 3 
print(e)

# integer division - quotient 
q = 2 // 3
print(q)

# modulo operation - remainder 
r = 2 % 3
print(r) 

